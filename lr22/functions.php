/**
 * Задание №6: Пользовательская функция
 * Расчет формулы y = (x^2 - 4x + 3) / (1 - x)
 */
function calculateFormula($x) {
    // Проверка исключительной ситуации: деление на ноль
    if ($x == 1) {
        return "Ошибка: деление на ноль (при x = 1)";
    }
    
    // Расчет числителя
    $numerator = pow($x, 2) - 4 * $x + 3;
    // Расчет знаменателя
    $denominator = 1 - $x;
    
    $y = $numerator / $denominator;
    return $y;
}
?>